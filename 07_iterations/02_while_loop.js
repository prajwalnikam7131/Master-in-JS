// The While Loop : The while loop loops through a block of code as long as a specified condition is true.

// syntax:
// while (condition) {
// }


// e.g.
let index = 0;

while (index <= 10) {

    console.log(index);
    index++;
}


//  e.g.
let myArray = ['flash', "batman", "superman"];

let arr = 0;
while (arr <= myArray.length) {

    console.log(myArray[arr]);
    arr++;
}