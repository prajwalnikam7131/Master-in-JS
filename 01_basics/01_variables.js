// # Variables : variables are containers for storing data/value.
// # JavaScript variables can be declared in 4 ways :- Automatically, using var, using let, using const.

// ## What is var, let and const ?
// # var, let and const are the keywords used for declaring the variables.

// # var is global scope.
// # let is block scope.
// # const is block scope, const cannot be Redeclared and cannot be Reassigned.

var firstName = 'ram';
let accountEmail = 'someone@gmail.com';
const accountId = 532563;

console.log(accountEmail);

