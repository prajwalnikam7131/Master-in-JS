// ## JavaScript Operators : Javascript operators are used to perform different types of mathematical and logical computations.

// # Types of JavaScript Operators : Arithmetic, Assignment, comparison, string, Logical, Bitwise and Ternary Operators.

// # Arithmetic Operators : 

let a = 5;
let b = 4;

console.log(a + b);  // 9  (Addition)
console.log(a - b);  // 1  (Subtraction)
console.log(a * b);  // 20 (Multiplication)
console.log(a ** b); // 5x5x5x5 = 625 (Power Operator) 
console.log(a / b);  // 1.25 (Divison)
console.log(a % b);  //  1 (Remainder/ modulo operator)


// # Increment Operator : 

let x = 10;
console.log(x); // 10

x++;            // post-Increment
let z = x;
console.log(z); // 11

let v = 5;
console.log(v); // 5

++v;            // Pre-Increment
let w = v;
console.log(w); // 6


// # Decrement Operator :

let j = 15;
console.log(j); // 15

j--;            // Post-Decrement
let k = j;
console.log(k); // 14

let l = 15;
console.log(l); // 15

--l;            // Pre-Decrement
let m = l;
console.log(m); // 14


