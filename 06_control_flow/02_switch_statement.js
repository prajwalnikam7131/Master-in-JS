// ## The switch statement is used to perform different actions based on different conditions.

// # Switch Statement : Use the switch statement to select one of many code blocks to be executed.

let month = 2;

switch (month) {
    case 1:
        console.log('January, 31 Days Month.');
        break;
    case 2:
        console.log('February, 28 Days Month.');
        break;
    case 3:
        console.log('March, 31 Days Month.');
        break;
    case 4:
        console.log('April, 30 Days Month.');
        break;

    default:
        break;
}